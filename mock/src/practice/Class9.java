package practice;

public class Class9 {
	//when we have more than one code block and we want to run only one from this then we use switch 
	//ion switch we have to give the number of case that we want to execute
	//break is used to break or stop that code
	public static void main(String[] args) 
	{
		switch(6)
		{
		case 1: System.out.println("monday");
		break;
		case 2: System.out.println("tuesday");
		break;
		case 3: System.out.println("wednesday");
		break;
		default : System.out.println("wrong input");
		}
		
		
	}

}
